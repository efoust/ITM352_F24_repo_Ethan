import handyMath2 as HM

num1 = float(input("provide a number please:" ))
num2 = float(input("provide a number please: "))
midpointValue = HM.midpoint(num1,num2)
print("midpoint= ",midpointValue)
squarerootValue = HM.squareroot(num1)
print("squareroot = ", squarerootValue)
exponentValue = HM.exponent(num1,num2)
print("exponent function= ", exponentValue)
maxValue = HM.max(num1,num2)
print("the max value= ", maxValue)
minValue = HM.min(num1,num2)
print("the min value= ", minValue)



