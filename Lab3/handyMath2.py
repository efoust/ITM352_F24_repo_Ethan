
def midpoint(num1,num2):
    return ((num1+num2)/2)
def squareroot(num1):
    return (num1**0.5)
def exponent(num1,num2):
    return (num1**num2)
def max(num1, num2):
    return (num1 > num2) * num1 + (num2 >= num1) * num2
def min(num1, num2):
    return (num1 < num2) * num1 + (num2 <= num1) * num2
